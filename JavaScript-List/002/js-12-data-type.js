// 1. String 字符串必须用引号包起来

let myName = "小明";

// 2. Number 数字直接写

let price = 99.9;

// 3. Boolean 布尔值，只有两个

let isAdult = true;
let isChild = false;

// 4. Array 数组：一个列表，用方括号括起来，里面可以放任何东西
let myFruits = ["苹果", "香蕉", "西瓜"];
console.log(myFruits[0]);

// 5. Object 对象：一个“描述”某种事物的集合，用大括号包起来
let person = {
  name: "小红",
  age: 18,
  isStudent: true
};
console.log(person.name);   // 输出：小红
